package DTO;

public class StatistikaDTO {
	
	
	private String ime;
	private String prezime;
	private double kartica;
	private double gotovina;
	private double ukupno;
	public String getIme() {
		return ime;
	}
	public void setIme(String ime) {
		this.ime = ime;
	}
	public String getPrezime() {
		return prezime;
	}
	public void setPrezime(String prezime) {
		this.prezime = prezime;
	}
	public double getKartica() {
		return kartica;
	}
	public void setKartica(double kartica) {
		this.kartica = kartica;
	}
	public double getGotovina() {
		return gotovina;
	}
	public void setGotovina(double gotovina) {
		this.gotovina = gotovina;
	}
	public double getUkupno() {
		return ukupno;
	}
	public void setUkupno(double ukupno) {
		this.ukupno = ukupno;
	}
	
	
}
